public class SupplierInfo {
	private int did;
	private String dname;
	private String dtel;
	private String demail;
	private String dadd;
	private String goodsName;
	private int goodsCategoryId;
	private String goodspictures;
	public int getDid() {
		return did;
	}
	public void setDid(int did) {
		this.did = did;
	}
	public String getDname() {
		return dname;
	}
	public void setDname(String dname) {
		this.dname = dname;
	}
	public String getDtel() {
		return dtel;
	}
	public void setDtel(String dtel) {
		this.dtel = dtel;
	}
	public String getDemail() {
		return demail;
	}
	public void setDemail(String demail) {
		this.demail = demail;
	}
	public String getDadd() {
		return dadd;
	}
	public void setDadd(String dadd) {
		this.dadd = dadd;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public int getGoodsCategoryId() {
		return goodsCategoryId;
	}
	public void setGoodsCategoryId(int goodsCategoryId) {
		this.goodsCategoryId = goodsCategoryId;
	}
	public String getGoodspictures() {
		return goodspictures;
	}
	public void setGoodspictures(String goodspictures) {
		this.goodspictures = goodspictures;
	}
	
	
	
	
	
	

}
